package br.com.controlequipamentos.pojo;

public class RelatorioEquipamento {

	
	private static final long serialVersionUID = -9163826571643132969L;
	private String id;
	private String nome;
	private String modelo;
	private String servstag;
	private String patrimonio;
	private String categoria;
	private String marca;
	private String idSetor;
	private String funcionario;
	private String situacao;
	
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public String getModelo() {
		return modelo;
	}
	public void setModelo(String modelo) {
		this.modelo = modelo;
	}
	public String getServstag() {
		return servstag;
	}
	public void setServstag(String servstag) {
		this.servstag = servstag;
	}
	public String getPatrimonio() {
		return patrimonio;
	}
	public void setPatrimonio(String patrimonio) {
		this.patrimonio = patrimonio;
	}
	public String getCategoria() {
		return categoria;
	}
	public void setCategoria(String categoria) {
		this.categoria = categoria;
	}
	public String getMarca() {
		return marca;
	}
	public void setMarca(String marca) {
		this.marca = marca;
	}
	public String getIdSetor() {
		return idSetor;
	}
	public void setIdSetor(String idSetor) {
		this.idSetor = idSetor;
	}
	
	public String getFuncionario() {
		return funcionario;
	}
	public void setFuncionario(String funcionario) {
		this.funcionario = funcionario;
	}
	public String getSituacao() {
		return situacao;
	}
	public void setSituacao(String situacao) {
		this.situacao = situacao;
	}
	public static long getSerialversionuid() {
		return serialVersionUID;
	}
	
	
	

}
