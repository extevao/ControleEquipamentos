package br.com.controlequipamentos.pojo;

public class ManutencaoEquipamento {

	
	private String titulo;
	private String descricao;
	private String dataInicio;
	private String dataFim;
	private String tempoManutencao;	
	private String modelo;
	private String marca;
	private String estadoManutencao;
	private String usuario;
	private String funcionarioMaquina;
	private String setor;
	
	public ManutencaoEquipamento() {
		
	}

	public String getTitulo() {
		return titulo;
	}

	public void setTitulo(String titulo) {
		this.titulo = titulo;
	}

	public String getDescricao() {
		return descricao;
	}

	public void setDescricao(String descricao) {
		this.descricao = descricao;
	}

	public String getDataInicio() {
		return dataInicio;
	}

	public void setDataInicio(String dataInicio) {
		this.dataInicio = dataInicio;
	}

	public String getDataFim() {
		return dataFim;
	}

	public void setDataFim(String dataFim) {
		this.dataFim = dataFim;
	}

	public String getTempoManutencao() {
		return tempoManutencao;
	}

	public void setTempoManutencao(String tempoManutencao) {
		this.tempoManutencao = tempoManutencao;
	}

	public String getModelo() {
		return modelo;
	}

	public void setModelo(String modelo) {
		this.modelo = modelo;
	}

	public String getMarca() {
		return marca;
	}

	public void setMarca(String marca) {
		this.marca = marca;
	}

	public String getEstadoManutencao() {
		return estadoManutencao;
	}

	public void setEstadoManutencao(String estadoManutencao) {
		this.estadoManutencao = estadoManutencao;
	}

	public String getUsuario() {
		return usuario;
	}

	public void setUsuario(String usuario) {
		this.usuario = usuario;
	}

	public String getFuncionarioMaquina() {
		return funcionarioMaquina;
	}

	public void setFuncionarioMaquina(String funcionarioMaquina) {
		this.funcionarioMaquina = funcionarioMaquina;
	}

	public String getSetor() {
		return setor;
	}

	public void setSetor(String setor) {
		this.setor = setor;
	}


}
