package br.com.controlequipamentos.pojo;

public class RelatorioGeralManutencaoMarca {
	
	private String idMarca;
	private String marca;
	private String qtdManutencao;
	private String qtdEquipamento;
	private float mediaManutencaoEquipamento;
	private float mediaTempoManutencao;
	private String tempoManutencao;
	
	
	
	public String getIdMarca() {
		return idMarca;
	}
	public void setIdMarca(String idMarca) {
		this.idMarca = idMarca;
	}
	public String getMarca() {
		return marca;
	}
	public void setMarca(String marca) {
		this.marca = marca;
	}
	public String getQtdManutencao() {
		return qtdManutencao;
	}
	public void setQtdManutencao(String qtdManutencao) {
		this.qtdManutencao = qtdManutencao;
	}
	public String getTempoManutencao() {
		return tempoManutencao;
	}
	public void setTempoManutencao(String tempoManutencao) {
		this.tempoManutencao = tempoManutencao;
	}
	public String getQtdEquipamento() {
		return qtdEquipamento;
	}
	public void setQtdEquipamento(String qtdEquipamento) {
		this.qtdEquipamento = qtdEquipamento;
	}
	public float getMediaManutencaoEquipamento() {
		return mediaManutencaoEquipamento;
	}
	public void setMediaManutencaoEquipamento(float mediaManutencaoEquipamento) {
		this.mediaManutencaoEquipamento = mediaManutencaoEquipamento;
	}
	public float getMediaTempoManutencao() {
		return mediaTempoManutencao;
	}
	public void setMediaTempoManutencao(float mediaTempoManutencao) {
		this.mediaTempoManutencao = mediaTempoManutencao;
	}
	
	

	
	
	
	
}
